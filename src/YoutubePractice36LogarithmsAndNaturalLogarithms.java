public class YoutubePractice36LogarithmsAndNaturalLogarithms {

    //https://docs.oracle.com/javase/7/docs/api/

    public static void main(String[] args){
        //when you take log of a number, it really means the natural logarithm
        //Math.log will return natural logarithm
        //log10 will provide non-natural logarithm; base 10

        System.out.println(Math.log(5.0));

        System.out.println(Math.log10(5.0));

        System.out.println(Math.log(Math.E));

        //negative numbers will return NaN

        //0 will provide you negative infinity


    }
}
