public class YoutubePractice37DegreesRadiansConverting {
    //https://docs.oracle.com/javase/7/docs/api/

    public static void main(String[] args){

        System.out.println(Math.toDegrees(7));
        System.out.println(Math.toDegrees(-7));//negs work as well

        System.out.println(Math.toDegrees(Math.PI));

        System.out.println(Math.toDegrees(Math.PI/2));

        System.out.println(Math.toDegrees(Math.PI/4));

        //toRadians:

        System.out.println(Math.toRadians(72));
        System.out.println(Math.toRadians(-72)); //negs work here too

        System.out.println(Math.toRadians(180)); //PI radians
    }
}
